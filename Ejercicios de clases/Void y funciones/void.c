void imprimir_suma_1_hasta_yfelizcumple (int hasta){ 
    int i;
    i=1;
    while(i <= hasta){
        printf("%i\n",i);
        i+=1;
    }
    printf("Feliz cumple\n");
}
int main(){
    imprimir_suma_1_hasta_yfelizcumple(100);
    return 0;
}
